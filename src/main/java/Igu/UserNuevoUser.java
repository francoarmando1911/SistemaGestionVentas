    private void btnAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUsuarioActionPerformed
       String usuario = txtNombreR.getText();
        String contra = new String(txtContrasenaR.getPassword());
        String rol = (String) cmbRol.getSelectedItem();

        control.crearUsuario(usuario,contra,rol);

        mostrarMensaje("Usuario creado correctamente", "Info", "Creacion exitosa");

        UserPerfiles usser = new UserPerfiles(control, usr); // Pasar control y usr como argumentos
        usser.setVisible(true);
        usser.setLocationRelativeTo(null);
        
        this.dispose();
    }//GEN-LAST:event_btnAgregarUsuarioActionPerformed

